  static List<IndianFood> get popular {
    final seen = <String>{};
    final result = <IndianFood>[];
    for (final f in foods) {
      if (result.length >= 30) break;
      if (!seen.contains(f.category)) {
        seen.add(f.category);
        result.add(f);
      }
    }
    for (final f in foods) {
      if (result.length >= 30) break;
      if (!result.contains(f)) result.add(f);
    }
    return result;
  }

  static List<IndianFood> byCategory(String category) {
    if (category == 'All') return foods;
    return foods.where((f) => f.category == category).toList();
  }

  static List<IndianFood> search(String query) {
    if (query.isEmpty) return [];
    final q = query.toLowerCase().trim();
    final results = foods
        .where((f) =>
            f.name.toLowerCase().contains(q) ||
            f.category.toLowerCase().contains(q))
        .toList();
    results.sort((a, b) {
      final aName = a.name.toLowerCase();
      final bName = b.name.toLowerCase();
      if (aName == q) return -1;
      if (bName == q) return 1;
      if (aName.startsWith(q) && !bName.startsWith(q)) return -1;
      if (bName.startsWith(q) && !aName.startsWith(q)) return 1;
      return aName.compareTo(bName);
    });
    return results;
  }
