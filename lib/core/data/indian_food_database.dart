/// Comprehensive Indian food nutrition database.
/// All nutritional values are per 100g (or 100ml for liquids).
/// Serving size is the typical portion used for one serving.

class IndianFood {
  final String name;
  final String category;
  final double calories;      // kcal per 100g
  final double protein;       // g per 100g
  final double carbs;         // g per 100g
  final double fat;           // g per 100g
  final String serving;       // human-readable serving description
  final double servingGrams;  // grams in one typical serving
  final double fiber;         // g per 100g
  final double sugar;         // g per 100g
  final double sodium;        // mg per 100g
  // ── Micronutrients (per 100g) ─────────────────────────────────
  final double iron;          // mg
  final double calcium;       // mg
  final double vitaminB12;    // mcg
  final double vitaminD;      // IU
  final double vitaminC;      // mg
  final double magnesium;     // mg
  // ── Glycemic ─────────────────────────────────────────────────
  final double glycemicIndex; // 0–100 scale

  const IndianFood({
    required this.name,
    required this.category,
    required this.calories,
    required this.protein,
    required this.carbs,
    required this.fat,
    required this.serving,
    required this.servingGrams,
    this.fiber = 0.0,
    this.sugar = 0.0,
    this.sodium = 0.0,
    this.iron = 0.0,
    this.calcium = 0.0,
    this.vitaminB12 = 0.0,
    this.vitaminD = 0.0,
    this.vitaminC = 0.0,
    this.magnesium = 0.0,
    this.glycemicIndex = 55.0,
  });

  /// Nutrition values scaled to a given gram weight.
  IndianFood scaled(double grams) => IndianFood(
        name: name,
        category: category,
        calories: calories * grams / 100,
        protein: protein * grams / 100,
        carbs: carbs * grams / 100,
        fat: fat * grams / 100,
        serving: serving,
        servingGrams: grams,
        fiber: fiber * grams / 100,
        sugar: sugar * grams / 100,
        sodium: sodium * grams / 100,
        iron: iron * grams / 100,
        calcium: calcium * grams / 100,
        vitaminB12: vitaminB12 * grams / 100,
        vitaminD: vitaminD * grams / 100,
        vitaminC: vitaminC * grams / 100,
        magnesium: magnesium * grams / 100,
        glycemicIndex: glycemicIndex,
      );

  /// Convert to CalorieEntry for logging
  CalorieEntryData toCalorieData() => CalorieEntryData(
        iron: iron,
        calcium: calcium,
        vitaminB12: vitaminB12,
        vitaminD: vitaminD,
        vitaminC: vitaminC,
        magnesium: magnesium,
        glycemicIndex: glycemicIndex,
      );
}

/// Thin carrier so FoodSearchScreen can pass micro data to CalorieEntry.
class CalorieEntryData {
  final double iron, calcium, vitaminB12, vitaminD, vitaminC, magnesium;
  final double glycemicIndex;
  const CalorieEntryData({
    this.iron = 0, this.calcium = 0, this.vitaminB12 = 0,
    this.vitaminD = 0, this.vitaminC = 0, this.magnesium = 0,
    this.glycemicIndex = 55,
  });
}

